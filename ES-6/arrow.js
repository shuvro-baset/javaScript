// using arrow function (=>) we can call a function in a single line
// after arrow sign we write something that will return what we want.

// function_name => return --- this is the syntax for arrow function
const doubleIt = num => num*5;
console.log(doubleIt(10));


// if we use more parameter then we need to use braces
const multiParam = (x,y) => x+y;
console.log(multiParam(23,75))
